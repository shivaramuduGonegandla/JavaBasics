package Loops;

//7. Write a program to print numbers from 1 to 10 using the do-while loop statement

public class DoWhileLoop {
    
	public static void main(String[] args) {
        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i <= 10);
    }
}

