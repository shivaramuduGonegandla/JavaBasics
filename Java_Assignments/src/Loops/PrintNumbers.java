package Loops;

// 2. Write a Java program to print numbers from 1 to 20 using the while loop

public class PrintNumbers {
  
	public static void main(String[] args) {
        int i = 1;
        while (i <= 20) {
            System.out.println(i);
            i++;
        }
    }
}

